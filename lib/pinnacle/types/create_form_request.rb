# frozen_string_literal: true

module Pinnacle
  module Types
    module CreateFormRequest
      # CreateFormRequest is an alias for FormDefinition

      # @option str [String]
      #
      # @return [untyped]
      def self.load(str)
        ::JSON.parse(str)
      end

      # @option value [untyped]
      #
      # @return [String]
      def self.dump(value)
        ::JSON.generate(value)
      end
    end
  end
end
